    def handle(self, *args, **options):
        """Fill geography tables."""
        try:
            self.stdout.write('üó∫Ô∏è Filling Ukraine geography tables...')
            
            if options['force']:
                self.stdout.write('üóëÔ∏è Clearing existing data...')
                CityModel.objects.all().delete()
                RegionModel.objects.all().delete()
            
            with transaction.atomic():
                self._create_regions()
                self._create_cities()
            
            # Print results
            regions_count = RegionModel.objects.count()
            cities_count = CityModel.objects.count()
            
            self.stdout.write(f'‚úÖ Geography filled successfully!')
            self.stdout.write(f'   Regions: {regions_count}')
            self.stdout.write(f'   Cities: {cities_count}')
            
        except Exception as e:
            self.stdout.write(self.style.ERROR(f'‚ùå Error filling geography: {e}'))
            raise

